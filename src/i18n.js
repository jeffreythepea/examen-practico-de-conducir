export const STRINGS = Object.freeze({ en: ENGLISH, es: SPANISH });

export function translate(locale, key, variables = {}) {
  if (!SUPPORTED_LOCALES.includes(locale)) throw new Error(`Unsupported locale: ${locale}`);
  const template = STRINGS[locale][key];
  if (template === undefined) throw new Error(`Missing translation: ${locale}.${key}`);
  return template.replace(/\{([^}]+)\}/g, (_match, name) => {
    if (!(name in variables)) throw new Error(`Missing variable: ${name}`);
    return String(variables[name]);
  });
}

export function setDocumentLocale(locale, documentRef = document) {
  if (!SUPPORTED_LOCALES.includes(locale)) throw new Error(`Unsupported locale: ${locale}`);
  documentRef.documentElement.lang = locale;
}
